const CENTER_LON = 133.5
const CENTER_LAT = -25.8
const LAT_COS = Math.cos((26.5 * Math.PI) / 180)

// Sets the continent's width in world units (~15 across the mainland).
export const CONTINENT_SCALE = 0.418

// worldX east, worldZ south (north = -z). `scale` sets continent width in world units.
function project(lon: number, lat: number, scale: number): [number, number] {
  const x = (lon - CENTER_LON) * LAT_COS * scale
  const z = -(lat - CENTER_LAT) * scale
  return [x, z]
}

export function outlinePoints(scale: number): { mainland: [number, number][]; tasmania: [number, number][] } {
  return {
    mainland: MAINLAND.map(([lon, lat]) => project(lon, lat, scale)),
    tasmania: TASMANIA.map(([lon, lat]) => project(lon, lat, scale)),
  }
}

